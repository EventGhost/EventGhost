def _HRESULT_TYPEDEF_(_sc):
    return HRESULT(_sc)


def AUDCLNT_ERR(code):
    return (SEVERITY_ERROR << 31) | (FACILITY_AUDCLNT << 16) | code


def AUDCLNT_SUCCESS(code):
    return (SEVERITY_SUCCESS << 31) | (FACILITY_AUDCLNT << 16) | code


def CTL_CODE(DeviceType, Function, Method, Access):
    return DeviceType << 16 | Access << 14 | Function << 2 | Method


def _convert_guid(*hex_guid):
    guid = []
    hex_four = ''
    hex_twelve = ''
    for h in hex_guid:
        h = hex(h)[2:].replace('L', '')

        if len(h) not in (8, 4, 2):
            h = '0' + h

        if len(h) == 2:
            if hex_four is not None and hex_four:
                h = hex_four + h
                hex_four = None
            elif hex_four is None:
                hex_twelve += h
                if len(hex_twelve) == 12:
                    h = hex_twelve
                else:
                    continue
            else:
                hex_four = h
                continue
        guid += [h.upper()]

    guid = '{' + '-'.join(guid) + '}'
    return guid


def DEFINE_PROPERTYKEY(*hex_guid):
    guid = _convert_guid(*hex_guid[:-1])
    index = hex_guid[-1]

    return PROPERTYKEY(GUID(guid), index)


def DEFINE_GUIDNAMED(*hex_guid):
    return GUID(_convert_guid(*hex_guid))


